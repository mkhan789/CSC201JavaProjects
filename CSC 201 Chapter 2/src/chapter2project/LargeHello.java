// Printing the word "HELLO" vertically using asterisks.

package chapter2project;

/*
 *  This program computes the words hello vertically in large letters using this symbol "*".
 */
public class LargeHello {
	public static void main(String[] args) {
	
		String letter_H = "* *\n***\n* *"; 
		
		System.out.println(letter_H);
		
		System.out.println("");
		
		String letter_E = "   ***\n   ** \n   ***";
		
		System.out.println(letter_E);
		
		System.out.println("");
		
		String letter_L = "   *  \n   *  \n   ***";
		
		System.out.println(letter_L);
		
		System.out.println("");
		
		String second_L = "   *  \n   *  \n   ***";
		
		System.out.println(second_L);
		
		System.out.println("");
		
		String letter_O = "   ***\n   * *\n   ***";
		
		System.out.println(letter_O);
	}
}